#!/usr/bin/perl -w
# integer arithmatic
my ($width, $length) = (5,6);
my $area=$length * $width;
print "The area of a square (width=$width, length=$length) is $area.\n";
  
# boolean logic
my ($true, $false) = (1,0);
my $result=$true && $false || $true;
print "The statement (true AND false OR true) is $result.\n";
