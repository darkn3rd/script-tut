#!/usr/bin/env groovy
# declare the variables
num    = 5                   # python int datatype
char   = 'a'                 # python string datatype
string = "This is a string"  # python string datatype
 
# output variables using interpolation with % operator
print "Number is %d." % num
print "Character is a '%c'." % char
print 'String is "%s".' % string
